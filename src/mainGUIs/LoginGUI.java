    private void loginBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBTNActionPerformed
        // TODO add your handling code here:
        String username = usernameTF.getText();
        String password = passwordTF.getText();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username and password must not be empty.");
        }
        
        try{
            User user = Authentication.checkLogin(username, password);
            if(user != null){
                switch(user.getRole()){
                    case "admin":
                        AdminGUI adminGUI = new AdminGUI();
                        adminGUI.show(true);
                        this.dispose();
                        break;
                    case "teacher":
                        TeacherGUI teacherGUI = new TeacherGUI();
                        teacherGUI.show(true);
                        this.dispose();
                        break;
                    case "student":
                        StudentGUI studentGUI = new StudentGUI();
                        studentGUI.show(true);
                        this.dispose();
                        break;
                    default:
                        JOptionPane.showMessageDialog(this, "Something went wrong. User role is invalid.");
                        break;
                }
            }else{
                JOptionPane.showMessageDialog(this, "Invalid Credintials");
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_loginBTNActionPerformed

